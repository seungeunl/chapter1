package com.javaex.ex01;

public class Ex01 {
	
	public static void main(String[] args) {
		
		int myAge; // 정수 넣을 공간을 잡아줘, 공간의 별명은 myAge(공간은 실행을 해야 잡힘.)
		myAge = 25; // myAge라는 공간에 25를 넣어줘 (오른쪽에 있는 것을 왼쪽에 넣음.)
		System.out.println(myAge); // myAge라는 공간에 있는 값을 화면에 찍어줘
		
		myAge = 26;
		System.out.println(myAge);
		
		long bVar = 6789020034L;
		System.out.println(bVar);
		
		int var01;
		int var02;
		int var03;
		
		var01 = 10;
		var03 = 30;
		var02 = 20;
		
		System.out.println(var01);
		System.out.println(var02);
		System.out.println(var03);
		
		// int var01 = 10; //공간 만들고 값 넣는 것까지 한번에
	}
	
	
}
